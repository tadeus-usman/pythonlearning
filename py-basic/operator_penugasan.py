# Operator penugasan digunakan untuk memberikan atau memodifikasi nilai ke dalam sebuah variabel.

a = 1   # Sama dengan
print(a)

b = 1
b += 1 # tambah sama dengan
print(b) 

c = 1
c -= 1 # kurang sama dengan
print(c)

d = 1
d *= 2 # kali sama dengan
print(d)

e = 1
e /= 2 # bagi sama dengan
print(e)


f = 9
f %= 4 # sisa bagi sama dengan
print(f)


g = 2
g **= 4 # pangkat sama dengan
print(g)

h = 11
h //= 3  # Pembagian bulat sama dengan
print(h)